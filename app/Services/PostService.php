<?php

namespace App\Services;

use App\Post;
use Log;

class PostService
{
    public function destroy($id) {
        $delFlag = false;

        try {
            $post = Post::find($id);
            $post->delete();
            $delFlag = true;
        } catch (Exception $e) {
            Log($e);
        }
        
        return $delFlag;
    }

