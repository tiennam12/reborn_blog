    }

    public function show($id) {
        try {
            $post = Post::find($id);
        } catch (Exception $e) {
            return false;
        }

        return $post;
    }

    public function edit($id) {
        try {
            $post = Post::find($id);
        } catch (Exception $e) {
            return false;
        }

        return $post;
    }

    public function update($id, $data) { 
        try {
            $post = Post::find($id);
            $post->update($data);
        } catch (Exception $e) {
            return false;
        }
        
        return $post;
    }
}
